// Utility function to get states with N/A option for countries without states
export const getStatesWithNA = (country: string): string[] => {
    const states = getStatesForCountry(country);
    return states.length > 0 ? states : ['N/A'];
};

// Utility function to get the appropriate field label
export const getStateFieldLabel = (country: string): string => {
    const states = getStatesForCountry(country);
    if (states.length === 0) {
        return 'State / Province / District (N/A)';
    }
    return 'State / Province / District';
};

// Get unique calling codes sorted numerically
export const getUniqueCallingCodes = (): Array<{code: string, countries: string[]}> => {
    const codeMap = new Map<string, string[]>();
    
    countries.forEach(country => {
        const code = country.callingCode;
        if (codeMap.has(code)) {
            codeMap.get(code)!.push(country.name);
        } else {
            codeMap.set(code, [country.name]);
        }
    });
    
    // Convert to array and sort numerically by calling code
    return Array.from(codeMap.entries())
        .map(([code, countries]) => ({ code, countries }))
        .sort((a, b) => {
            // Extract numeric part for sorting
            const numA = parseInt(a.code.replace('+', ''));
            const numB = parseInt(b.code.replace('+', ''));
            return numA - numB;
        });
};
